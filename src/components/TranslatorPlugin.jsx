const TranslatorPlugin = () => {
  const [isTranslating, setIsTranslating] = useState(false);
  const [status, setStatus] = useState('idle');
  const [translatedCount, setTranslatedCount] = useState(0);

  const handleTranslate = () => {
    setIsTranslating(true);
    
    setTimeout(() => {
      // 1. GrapesJS Iframe'ini Bul (Doğru Seçici)
      const iframe = document.querySelector('iframe.gjs-frame');
      
      if (!iframe) {
        console.error("GrapesJS iframe bulunamadı!");
        setIsTranslating(false);
        return;
      }

      const doc = iframe.contentDocument || iframe.contentWindow.document;
      
      // 2. Görünen Metinleri Topla (Script ve Style hariç)
      const walker = doc.createTreeWalker(doc.body, NodeFilter.SHOW_TEXT, {
        acceptNode: (node) => {
          const parentName = node.parentNode.nodeName;
          if (['SCRIPT', 'STYLE', 'NOSCRIPT', 'CODE'].includes(parentName)) return NodeFilter.FILTER_REJECT;
          if (node.nodeValue.trim().length === 0) return NodeFilter.FILTER_SKIP;
          return NodeFilter.FILTER_ACCEPT;
        }
      }, false);

      let node;
      const nodes = [];
      while (node = walker.nextNode()) nodes.push(node);

      let count = 0;

      // 3. Agresif Çeviri Döngüsü
      nodes.forEach(textNode => {
        let text = textNode.nodeValue;
        let originalText = text;
        let hasChange = false;

        // Sözlükteki her kelimeyi kontrol et
        Object.keys(dictionary).forEach(key => {
          // Regex: Kelimeyi olduğu gibi ara (Büyük/küçük harf duyarsız)
          const regex = new RegExp(key, 'gi'); // \b sınırını kaldırdık, direkt eşleşme
          
          if (regex.test(text)) {
            // Eşleşen yeri sözlükteki karşılığıyla değiştir
            text = text.replace(regex, dictionary[key]);
            hasChange = true;
          }
        });

        if (hasChange && text !== originalText) {
          textNode.nodeValue = text;
          count++;
        }
      });

      setTranslatedCount(count);
      setIsTranslating(false);
      setStatus('success');
      
      setTimeout(() => setStatus('idle'), 3000);
    }, 500);
  };

  return (
    <div className="p-4 text-white">
      <h3 className="text-lg font-bold mb-4 flex items-center gap-2">
        <Languages className="text-blue-400" /> AI Çevirmen
      </h3>
      
      <p className="text-sm text-gray-400 mb-6">
        GrapesJS editöründeki İngilizce temayı otomatik algılar ve Türkçe'ye çevirir.
      </p>

      <div className="bg-gray-800 p-4 rounded-lg border border-gray-700 mb-6">
        <div className="flex items-center justify-between text-sm mb-2">
          <span className="text-gray-400">Kaynak</span>
          <span className="text-white font-medium">İngilizce (EN)</span>
        </div>
        <div className="flex justify-center my-2">
            <ArrowRightLeft size={16} className="text-gray-500" />
        </div>
        <div className="flex items-center justify-between text-sm">
          <span className="text-gray-400">Hedef</span>
          <span className="text-green-400 font-medium">Türkçe (TR)</span>
        </div>
      </div>

      <button
        onClick={handleTranslate}
        disabled={isTranslating}
        className={`w-full py-3 rounded-lg font-bold flex items-center justify-center gap-2 transition-all ${
          status === 'success' 
            ? 'bg-green-600 text-white' 
            : 'bg-blue-600 hover:bg-blue-500 text-white'
        }`}
      >
        {isTranslating ? (
          <> <Loader2 className="animate-spin" size={20} /> Çevriliyor... </>
        ) : status === 'success' ? (
          <> <Check size={20} /> {translatedCount} Öğe Çevrildi! </>
        ) : (
          <> <Languages size={20} /> Temayı Çevir </>
        )}
      </button>
      
      <div className="mt-4 text-xs text-center text-gray-500">
        *Genişletilmiş Sözlük (2000+ Kelime)
      </div>
    </div>
  );
};
export default TranslatorPlugin;